import 'package:gohomy/model/post_roommate.dart';

class PostRoommateRes {
  PostRoommateRes({
    this.code,
    this.success,
    this.msgCode,
    this.msg,
    this.data,
  });

  int? code;
  bool? success;
  String? msgCode;
  String? msg;
  PostRoommate? data;

  factory PostRoommateRes.fromJson(Map<String, dynamic> json) =>
      PostRoommateRes(
        code: json["code"],
        success: json["success"],
        msgCode: json["msg_code"],
        msg: json["msg"],
        data: json["data"] == null ? null : PostRoommate.fromJson(json["data"]),
      );

  Map<String, dynamic> toJson() => {
        "code": code,
        "success": success,
        "msg_code": msgCode,
        "msg": msg,
        "data": data?.toJson(),
      };
}
